<template>
  <!-- 利用規約 -->
  <h2>第1条：本サービスの利用にあたっての定義</h2>
  <p>本規約において使用される用語の定義は、以下のとおりとする。</p>
  <ul>
    <li>「本サービス」とは、当社が提供するサービスを指す。</li>
    <li>「利用者」とは、本サービスを利用する個人または団体を指す。</li>
    <li>「登録情報」とは、利用者が本サービスを利用するために提供する情報を指す。</li>
  </ul>

  <h2>第2条：本サービスの利用にあたっての登録情報の提供</h2>
  <p>利用者は、本サービスを利用するにあたり、適切な方法で登録情報を提供するものとする。</p>
  <p>登録情報には、以下の事項が含まれる。</p>
  <ul>
    <li>氏名</li>
    <li>住所</li>
    <li>電話番号</li>
    <li>メールアドレス</li>
  </ul>

  <h2>第3条：本サービスの利用にあたっての他の利用者の権利の尊重</h2>
  <p>利用者は、本サービスを利用するにあたり、他の利用者の権利を侵害しないものとする。</p>
  <p>特に、以下の行為は禁止とする。</p>
  <ul>
    <li>他の利用者の個人情報を収集または公開する行為</li>
    <li>他の利用者の著作権を侵害する行為</li>
    <li>他の利用者の名誉を毀損する行為</li>
  </ul>

  <h2>第4条：本サービスの利用にあたっての個人情報の取り扱い</h2>
  <p>利用者は、本サービスを利用するにあたり、適切な方法で個人情報を取り扱うものとする。</p>
  <p>個人情報には、以下の事項が含まれる。</p>
  <ul>
    <li>氏名</li>
    <li>住所</li>
    <li>電話番号</li>
    <li>メールアドレス</li>
  </ul>

  <h2>第5条：本サービスの利用にあたっての料金の支払い</h2>
  <p>利用者は、本サービスを利用するにあたり、適切な方法で料金を支払うものとする。</p>
  <p>料金には、以下の事項が含まれる。</p>
  <ul>
    <li>月額料金</li>
    <li>利用料金</li>
  </ul>
